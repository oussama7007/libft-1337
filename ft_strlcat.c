#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	s;
	size_t	d;
	size_t	di;
	size_t	s_len;

	s_len = ft_strlen(src);
	if (!dst && size == 0)
		return (s_len);
	d = ft_strlen(dst);
	di = d;
	if (size <= di)
		return (size + s_len);
	s = 0;
	while (src[s] && d + 1 < size)
	{
		dst[d] = src[s];
		s++;
		d++;
	}
	dst[d] = 0;
	return (di + s_len);
}
